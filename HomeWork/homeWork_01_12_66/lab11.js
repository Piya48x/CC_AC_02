let [first, second, third] = ["Maya", "Marisa", "Chi"];
console.log(first); // *
console.log(second); // **
console.log(third); // ***
/** เป็นการ destructuring assignment ของ Array ซึ่งใช้ let [first, second, third] เพื่อกำหนดค่าให้กับตัวแปร first, second, และ third จาก Array ['Maya', 'Marisa', 'Chi'] ตามลำดับ
 * ต่อมามีการใช้ console.log เพื่อแสดงผลลัพธ์ที่ได้จากการเข้าถึงตัวแปร first, second, และ third ตามลำดับ:
 * ผลลัพธ์ที่แสดงบน console คือ:
 *  Maya   // * (ตัวแปร first)
    Marisa // ** (ตัวแปร second)
    Chi    // *** (ตัวแปร third)

*/
